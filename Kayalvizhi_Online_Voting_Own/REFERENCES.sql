DECLARE
  TYPE V_AADHAAR IS TABLE OF VARCHAR(32);
  VO_AADHAAR V_AADHAAR;
  VO_FIRST_NAME V_AADHAAR; 
BEGIN
  SELECT 
    AADHAAR,
    FIRST_NAME 
  BULK COLLECT INTO 
    VO_AADHAAR,
    VO_FIRST_NAME 
  FROM 
    VOTER;
  FOR IDX IN 1..VO_AADHAAR.COUNT LOOP
    DBMS_OUTPUT.PUT_LINE(VO_AADHAAR(IDX)|| '  '||FIRSTNAME(IDX));
  END LOOP;
END;
