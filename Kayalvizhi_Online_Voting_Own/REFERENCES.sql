DECLARE
    CURSOR EXP_CUR 
    IS
      SELECT 
      FIRST_NAME,
      LAST_NAME 
    FROM 
      VOTER;
    TYPE V_FNAME IS TABLE OF VARCHAR(32);
    FNAME V_FNAME;
    LNAME V_FNAME;
BEGIN
  OPEN EXP_CUR;
    FETCH EXP_CUR BULK COLLECT INTO FNAME,LNAME LIMIT 10;
    FOR IDX IN FNAME.FIRST..FNAME.LAST
    LOOP
      DBMS_OUTPUT.PUT_LINE(IDX|| '  '||FNAME(IDX)||'   '||LNAME(IDX));
    END LOOP;
  CLOSE EXP_CUR;
END;
